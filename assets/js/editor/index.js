/**
 * Block editor integration — shows source release attribution
 * and regeneration controls in the Post Status panel.
 */

import { registerPlugin } from '@wordpress/plugins';
import { PluginPostStatusInfo } from '@wordpress/edit-post';
import { useEntityProp } from '@wordpress/core-data';
import { useSelect, useDispatch } from '@wordpress/data';
import { useState } from '@wordpress/element';
import { __ } from '@wordpress/i18n';
import { Button, ExternalLink, TextareaControl, Spinner } from '@wordpress/components';
import apiFetch from '@wordpress/api-fetch';

/**
 * Renders release attribution and regeneration controls in the
 * Status & Visibility panel for posts generated by this plugin.
 */
function ReleaseAttribution() {
	const postType = useSelect(
		( select ) => select( 'core/editor' ).getCurrentPostType(),
		[]
	);

	const postId = useSelect(
		( select ) => select( 'core/editor' ).getCurrentPostId(),
		[]
	);

	const [ meta ] = useEntityProp( 'postType', postType, 'meta' );
	const { editPost } = useDispatch( 'core/editor' );

	const [ showFeedback, setShowFeedback ] = useState( false );
	const [ feedback, setFeedback ] = useState( '' );
	const [ isRegenerating, setIsRegenerating ] = useState( false );
	const [ resultMessage, setResultMessage ] = useState( '' );

	if ( ! meta ) {
		return null;
	}

	const repo = meta._ctbp_source_repo || '';
	const tag = meta._ctbp_release_tag || '';
	const releaseUrl = meta._ctbp_release_url || '';

	if ( ! repo || ! releaseUrl ) {
		return null;
	}

	function handleRegenerate() {
		setIsRegenerating( true );
		setResultMessage( '' );

		apiFetch( {
			path: '/ctbp/v1/releases/regenerate',
			method: 'POST',
			data: {
				post_id: postId,
				feedback: feedback.trim(),
			},
		} )
			.then( ( response ) => {
				setIsRegenerating( false );
				setShowFeedback( false );
				setFeedback( '' );
				setResultMessage( __( 'Content regenerated. Refresh to see changes.', 'changelog-to-blog-post' ) );

				// Update the post content in the editor.
				if ( response && response.post ) {
					editPost( {
						title: response.post.title,
					} );
					// Force a content refresh by dispatching a notice.
					wp.data.dispatch( 'core/notices' ).createSuccessNotice(
						__( 'Post content regenerated from release.', 'changelog-to-blog-post' ),
						{ type: 'snackbar' }
					);
				}
			} )
			.catch( ( error ) => {
				setIsRegenerating( false );
				setResultMessage(
					error.message || __( 'Regeneration failed.', 'changelog-to-blog-post' )
				);
			} );
	}

	return (
		<PluginPostStatusInfo className="ctbp-release-attribution">
			<div style={ { width: '100%' } }>
				<p style={ { margin: '0 0 8px' } }>
					{ __( 'Generated from', 'changelog-to-blog-post' ) }{ ' ' }
					<ExternalLink href={ releaseUrl }>
						{ __( 'GitHub plugin release', 'changelog-to-blog-post' ) }
					</ExternalLink>
					<span style={ { color: '#757575', marginLeft: '4px' } }>
						({ repo } { tag })
					</span>
				</p>

				{ ! showFeedback && (
					<Button
						variant="secondary"
						size="small"
						onClick={ () => setShowFeedback( true ) }
						disabled={ isRegenerating }
					>
						{ __( 'Regenerate', 'changelog-to-blog-post' ) }
					</Button>
				) }

				{ showFeedback && (
					<div style={ { marginTop: '8px' } }>
						<TextareaControl
							label={ __( 'Feedback (optional)', 'changelog-to-blog-post' ) }
							help={ __( 'Tell the AI what to change, e.g. "make it shorter" or "emphasize the security fix".', 'changelog-to-blog-post' ) }
							value={ feedback }
							onChange={ setFeedback }
							rows={ 3 }
							disabled={ isRegenerating }
						/>
						<div style={ { display: 'flex', gap: '8px', alignItems: 'center' } }>
							<Button
								variant="primary"
								size="small"
								onClick={ handleRegenerate }
								disabled={ isRegenerating }
							>
								{ isRegenerating
									? __( 'Regenerating…', 'changelog-to-blog-post' )
									: __( 'Regenerate content', 'changelog-to-blog-post' )
								}
							</Button>
							{ ! isRegenerating && (
								<Button
									variant="tertiary"
									size="small"
									onClick={ () => {
										setShowFeedback( false );
										setFeedback( '' );
									} }
								>
									{ __( 'Cancel', 'changelog-to-blog-post' ) }
								</Button>
							) }
							{ isRegenerating && <Spinner /> }
						</div>
					</div>
				) }

				{ resultMessage && (
					<p style={ { marginTop: '8px', color: '#00a32a', fontSize: '13px' } }>
						{ resultMessage }
					</p>
				) }
			</div>
		</PluginPostStatusInfo>
	);
}

registerPlugin( 'ctbp-release-attribution', {
	render: ReleaseAttribution,
} );
